    private void carregarProdutos() {
        ((DefaultTableModel)tabelaClientes.getModel()).setRowCount(0);
        try {
            ProdutoDAO produtoDAO = new ProdutoDAO();
            DefaultTableModel tableModel = (DefaultTableModel)tabelaClientes.getModel();
            for (Produto produto:produtoDAO.findAllProduto()){
                tableModel.addRow(new Object[]{produto.getArtigo(), produto.getDescricao() , produto.getCategoria(), produto.getQtde_produto(), produto.getPreco()});
            }
            produtoDAO.close();
        } catch (SQLException ex) {
            Logger.getLogger(ManterProdutos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void arrumarCadastro(){
        if (novoCadastro){
            campoArtigo.setVisible(false);
            campoDescricao.setText("");
            campoPreco.setText("");
            campoCategoria.setText("");
            campoQuantidade.setText("");
            labelTitle.setText("Novo cadastro de produto:");
            campoPreco.setEditable(true);
            botaoCancela.setVisible(true);
            botaoEditar.setVisible(false);
            botaoRemover.setVisible(false);
            botaoNovo.setText("Confirma");
        }else{
            labelTitle.setText("Editar dados dos produtos:");
            campoPreco.setEditable(false);
            botaoCancela.setVisible(false);
            botaoEditar.setVisible(true);
            botaoRemover.setVisible(true);
            campoArtigo.setVisible(true);
            botaoNovo.setText("Novo");
            campoDescricao.setText("");
            campoPreco.setText("");
            campoCategoria.setText("");
            campoArtigo.setText("");
            campoQuantidade.setText("");
        }
    }
