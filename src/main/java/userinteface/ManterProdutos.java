    private void botaoRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoRemoverActionPerformed
        try {
            ProdutoDAO produtoDAO = new ProdutoDAO();
            if (produtoDAO.deleteProduto(Integer.parseInt(campoArtigo.getText()))){
                campoDescricao.setText("");
                campoPreco.setText("");
                campoCategoria.setText("");
                campoArtigo.setText("");
                campoQuantidade.setText("");
                botaoRemover.setEnabled(false);
                botaoEditar.setEnabled(false);
                ((DefaultTableModel)tabelaClientes.getModel()).removeRow(tabelaClientes.getSelectedRow());
                JOptionPane.showMessageDialog(null, "Produto removido.");
            }
        } catch (SQLException ex) {
            Logger.getLogger(ManterProdutos.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_botaoRemoverActionPerformed

